//----------------------------------------------------------------------------------------------------------------------
UInt16 SNumber::getNextPowerOf2(UInt16 value)
//----------------------------------------------------------------------------------------------------------------------
{
	// Use bit operations to round up to the next power of 2 value
	value--;
	value |= (value >> 8);
	value |= (value >> 4);
	value |= (value >> 2);
	value |= (value >> 1);

	return ++value;
}

//----------------------------------------------------------------------------------------------------------------------
bool SNumber::randomBool()
//----------------------------------------------------------------------------------------------------------------------
{
	return (random() % 2) == 0;
}

//----------------------------------------------------------------------------------------------------------------------
Float32 SNumber::randomFloat32(Float32 min, Float32 max)
//----------------------------------------------------------------------------------------------------------------------
{
	// Setup
	Float32	delta = max - min;

	// Check delta
	if (delta == 0.0)
		// No delta
		return min;
	else if (delta > 0.0)
		// Delta is positive
		return (random() % (UInt32) delta) + min;
	else
		// Delta is negative (why?)
		return min - (random() % (UInt32) -delta);
}

//----------------------------------------------------------------------------------------------------------------------
UInt32 SNumber::randomUInt32(UInt32 min, UInt32 max)
//----------------------------------------------------------------------------------------------------------------------
{
	// Setup
	UInt32	delta = max - min;

	// Check delta
	if (delta == 0)
		// No delta
		return min;
	else if (delta > 0)
		// Delta is positive
		return (random() % delta) + min;
	else
		// Delta is negative (why?)
		return min - (random() % -delta);
}
