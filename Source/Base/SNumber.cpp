UInt16 gNextPowerOf2(UInt16 value)
//----------------------------------------------------------------------------------------------------------------------
{
	// Use bit operations to round up to the next power of 2 value
	value--;
	value |= (value >> 8);
	value |= (value >> 4);
	value |= (value >> 2);
	value |= (value >> 1);

	return ++value;
}
