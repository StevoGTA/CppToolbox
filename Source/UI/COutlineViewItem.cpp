#include "COutlineViewItem.h"

//----------------------------------------------------------------------------------------------------------------------
// MARK: Local procs

//----------------------------------------------------------------------------------------------------------------------
static bool sIsOutlineViewItemFoundInIDs(const I<COutlineViewItem>& outlineViewItem, TArray<CString>* ids)
//----------------------------------------------------------------------------------------------------------------------
{
	return ids->contains(outlineViewItem->getID());
}

//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
// MARK: - COutlineViewItem

// MARK: Class methods

//----------------------------------------------------------------------------------------------------------------------
TArray<I<COutlineViewItem> > COutlineViewItem::filtered(const TArray<I<COutlineViewItem> >& outlineViewItems,
		const TArray<CString> ids)
//----------------------------------------------------------------------------------------------------------------------
{
	return TNArray<I<COutlineViewItem> >(outlineViewItems,
			(TNArray<I<COutlineViewItem> >::IsMatchProc) sIsOutlineViewItemFoundInIDs, (void*) &ids);
}
